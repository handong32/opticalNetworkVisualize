package visualization;

public class SingleDataSet 
{
	private String name;
	private String fill;
	
	public SingleDataSet(String name, String fill)
	{
		this.name = name;
		this.fill = fill;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getFill() {
		return fill;
	}

	public void setFill(String fill) {
		this.fill = fill;
	}
	
	
}
